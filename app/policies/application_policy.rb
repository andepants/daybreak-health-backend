# Base Pundit policy for authorization
# All policy classes should inherit from this class
# Default behavior: deny all actions
#
# Example usage:
#   class OnboardingSessionPolicy < ApplicationPolicy
#     def show?
#       user.present? && record.id == user.session_id
#     end
#   end
class ApplicationPolicy
  attr_reader :user, :record

  def initialize(user, record)
    @user = user
    @record = record
  end

  def index?
    false
  end

  def show?
    false
  end

  def create?
    false
  end

  def new?
    create?
  end

  def update?
    false
  end

  def edit?
    update?
  end

  def destroy?
    false
  end

  class Scope
    def initialize(user, scope)
      @user = user
      @scope = scope
    end

    def resolve
      raise NoMethodError, "You must define #resolve in #{self.class}"
    end

    private

    attr_reader :user, :scope
  end
end
